export const getCardById = (id: string): TarotCard | undefined => {
  return tarotCards.find((c) => c.id === id);
};

export const getCardsByArcana = (arcana: 'major' | 'minor'): TarotCard[] => {
  return tarotCards.filter((c) => c.arcana === arcana);
};

export const getCardsBySuit = (suit: string): TarotCard[] => {
  return tarotCards.filter((c) => c.suit === suit);
};

export const getRandomCard = (): TarotCard => {
  return tarotCards[Math.floor(Math.random() * tarotCards.length)];
};

export const drawCards = (count: number): TarotCard[] => {
  const shuffled = [...tarotCards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
